namespace AntdUI
{
    public enum TAlignItems
    {
        /// <summary>
        /// 元素位于容器的中心
        /// </summary>
        Center,
        /// <summary>
        /// 元素位于容器的开头
        /// </summary>
        Start,
        /// <summary>
        /// 元素位于容器的结尾
        /// </summary>
        End
    }
    public enum TJustifyContent
    {
        /// <summary>
        /// 居中排列
        /// </summary>
        Center,
        /// <summary>
        /// 从行首起始位置开始排列
        /// </summary>
        Start,
        /// <summary>
        /// 从行尾位置开始排列
        /// </summary>
        End,
        /// <summary>
        /// 均匀排列每个元素，首个元素放置于起点，末尾元素放置于终点
        /// </summary>
        SpaceBetween,
        /// <summary>
        /// 均匀排列每个元素，每个元素之间的间隔相等
        /// </summary>
        SpaceAround,
        /// <summary>
        /// 均匀排列每个元素，每个元素周围分配相同的空间
        /// </summary>
        SpaceEvenly
    }
    public enum TAlignContent
    {
        /// <summary>
        /// 元素位于容器的中心
        /// </summary>
        Center,
        /// <summary>
        /// 元素位于容器的开头
        /// </summary>
        Start,
        /// <summary>
        /// 元素位于容器的结尾
        /// </summary>
        End,
        /// <summary>
        /// 元素位于各行之间留有空白的容器内
        /// </summary>
        SpaceBetween,
        /// <summary>
        /// 元素位于各行之前、之间、之后都留有空白的容器内
        /// </summary>
        SpaceAround,
        /// <summary>
        /// 均匀排列每个元素，每个元素周围分配相同的空间
        /// </summary>
        SpaceEvenly
    }
}